package PROGRAMAS_EM_JAVA.EXERCIOS_SALA.Exercicio08_1.Univercidade;

public abstract class Menbro {
    private String nome;
    private String data;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
